# Create a list of three friends' names
friends_names = ["Sophia", "Liam", "Ava"]

# Print the first friend
print("First friend:", friends_names[0])

# Print the last friend
print("Last friend:", friends_names[-1])

# Print the length of the list
print("Number of friends:", len(friends_names))


